  const i = +idx;
  if (!a[i]) { missing++; log.push(`MISSING ${i}`); continue; }
  if (a[i].tr !== tr) {
    log.push(`TR ${i}  ${JSON.stringify(a[i].tr)} -> ${JSON.stringify(tr)}`);
    a[i].tr = tr;
    trChanges++;
  }
